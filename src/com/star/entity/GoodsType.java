package com.star.entity;

//商品类别表
public class GoodsType {
	private String tid;//编号
	private String  name;//类别名
	
	public String getTid() {
		return tid;
	}
	public void setTid(String tid) {
		this.tid = tid;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
}
